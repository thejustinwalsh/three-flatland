export type SfxName = keyof typeof SFX

/** Minimum spacing prevents overlapping chunks from creating audio spikes. */
const MIN_INTERVAL_MS: Partial<Record<SfxName, number>> = {
  drill: 55,
  stoneDrill: 65,
  drillerLand: 90,
  blockFall: 140,
  blockLand: 120,
  explosion: 90,
  ghostWhoosh: 500,
  gemCollect: 35,
  sagWarning: 250,
  trigger: 45,
  crush: 300,
  respawn: 300,
}

export interface SoundPlayer {
  play(name: SfxName): void
}

export function createSoundPlayer(
  zzfx: PlaySoundFn,
  now: () => number = () => performance.now()
): SoundPlayer {
  const lastPlayedAt = new Map<SfxName, number>()

  return {
    play(name) {
      const time = now()
      const last = lastPlayedAt.get(name)
      const interval = MIN_INTERVAL_MS[name] ?? 0
      if (last !== undefined && time - last < interval) return
      lastPlayedAt.set(name, time)

      try {
        zzfx(...SFX[name])
      } catch {
        // Audio is optional and may still be locked behind a browser gesture.
      }
    },
  }
}

const worldPlayers = new WeakMap<World, SoundPlayer>()

/** Bind an unlocked ZzFX player to one simulation world. */
export function bindSoundPlayer(world: World, player: SoundPlayer): () => void {
  worldPlayers.set(world, player)
  return () => {
    if (worldPlayers.get(world) === player) worldPlayers.delete(world)
  }
}

/** Systems use this safe no-op bridge instead of depending on React. */
export function playSound(world: World, name: SfxName): void {
  worldPlayers.get(world)?.play(name)
}
